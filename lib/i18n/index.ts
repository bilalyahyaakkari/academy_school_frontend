export type TranslationKey = keyof (typeof dict)["en"];

/** Resolve a translation key for a given locale, falling back to English. */
export function translate(
  locale: Locale,
  key: TranslationKey,
  vars?: Record<string, string | number>,
): string {
  const localized = (dict[locale] as Record<string, string>)[key];
  let s = localized ?? dict.en[key] ?? key;
  if (vars) {
    s = s.replace(/\{(\w+)\}/g, (_, k) =>
      Object.prototype.hasOwnProperty.call(vars, k) ? String(vars[k]) : `{${k}}`,
    );
  }
  return s;
}

/** Format "May 2026" / "أيار 2026" depending on locale. */
const ARABIC_MONTHS_LEVANTINE = [
  "كانون الثاني",
  "شباط",
  "آذار",
  "نيسان",
  "أيار",
  "حزيران",
  "تموز",
  "آب",
  "أيلول",
  "تشرين الأول",
  "تشرين الثاني",
  "كانون الأول",
];

const ENGLISH_MONTHS = [
  "January", "February", "March", "April", "May", "June",
  "July", "August", "September", "October", "November", "December",
];

export function monthLabelI18n(
  locale: Locale,
  month: number,
  year: number,
): string {
  const name =
    locale === "ar"
      ? ARABIC_MONTHS_LEVANTINE[month - 1]
      : ENGLISH_MONTHS[month - 1];
  return `${name} ${year}`;
}

export function dirFor(locale: Locale): "ltr" | "rtl" {
  return locale === "ar" ? "rtl" : "ltr";
}
